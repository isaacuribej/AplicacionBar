    private void btnAnadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnadirActionPerformed
        // TODO add your handling code here:
        String producto = selector.getSelectedItem();
        String cantidadtxt = spiValue.getValue().toString();
        int cantidadint=(int) spiValue.getValue();
        String mesa= mesatxt.getText();
        String preciotxt= price.getText();
        int precioint=Integer.parseInt(preciotxt);
        int precioTotal=precioint*cantidadint;
        
        
        
        
        
        arrayProductos.add(producto);
        arrayProductosPantalla.add(producto+"           "+ cantidadtxt+"  uds");
        arrayCantidad.add(cantidadint);
        arrayMesas.add(mesa);
        arrayPrecio.add(precioint);
        arrayPrecioTotal.add(precioTotal);
        
        modelo.removeAllElements();
        
        
        for (int i=0 ;i<arrayProductosPantalla.size();i++){
            modelo.addElement(arrayProductosPantalla.get(i));
            
            
            
        }
        
        
        
    }//GEN-LAST:event_btnAnadirActionPerformed

