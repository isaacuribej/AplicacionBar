    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String texto="";
        
        String temp="";
        
        try {
            BufferedReader bf = new BufferedReader(new FileReader(""+archivoSeleccionado));
            
            String bfRead;
            while((bfRead=bf.readLine())!=null){
                temp=temp+bfRead+"\n";
            }
            
            texto=temp;
            
        } catch (FileNotFoundException ex) {
            Logger.getLogger(Pedidos.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) { 
            Logger.getLogger(Pedidos.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        System.out.println(texto);
        
        
        try {
            BufferedWriter fichero2 = new BufferedWriter(new FileWriter(""+archivoSeleccionado));
            fichero2.append(texto);
            
            for (int i=0;i<arrayProductos.size();i++){
                fichero2.append(Fecha+","+Hora+","+arrayProductos.get(i)+","+arrayCantidad.get(i)+","+arrayPrecio.get(i)+","+arrayMesas.get(i)+","+arrayPrecioTotal.get(i)+"\n");
                
            }
            fichero2.close();
            
        } catch (IOException ex) {
            Logger.getLogger(Pedidos.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

